      												<div class="mdl-tabs__panel" id="tab3-panel">
                                <h3 class="tab-h3">Mamografía</h3>
                                <section>
                                  <p>El <strong>único</strong> método recomendado y que demostró ser efectivo para la detección precoz del cáncer de mama es la <strong>mamografía.</strong>
                                    Durante la prueba la mama se comprime entre dos placas planas y se saca una radiografía. Algunas mujeres experimentan molestia o dolor según la sensibilidad de sus mamas.
                                  </p>
                                  <p><strong>Beneficio</strong></p>
                                  <p>El objetivo del tamizaje con mamografía es <strong>reducir las muertes por cåncer de mama</strong> al detectar y tratar lesiones de forma temprana. El tamizaje no evita el cáncer de mama.</p>
                                  <p><strong>Desventaja</strong></p>
                                  <p>El tamizaje con mamografía también tiene desventajas. Algunas mujeres tendrán <strong>falsas alarmas</strong> (deberán repetir estudios o realizar biopsias por lesiones benignas que no quedaban claras) o serán diagnosticadas y tratadas por un <strong>cáncer de mama que no hubiera producido síntomas o daño durante su vida.</strong></p>
                                  <p>Actualmente, el Programa Nacional de Control de Cáncer de Mama del Instituto Nacional del Cáncer de Argentina recomienda realizar una mamografía cada 2 años en mujeres de 50 a 69 años.</p>
